    private void tabelMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMahasiswaMouseClicked
        int baris = tabelMahasiswa.getSelectedRow();
        String npm = tabelMahasiswa.getValueAt(baris, 0).toString();
        if(!npm.isEmpty()){
            txtnpm.setText(npm);
            txtnama.setText(tabelMahasiswa.getValueAt(baris, 1).toString());
            cojk.setSelectedItem(tabelMahasiswa.getValueAt(baris, 2).toString());
            coprodi.setSelectedItem(tabelMahasiswa.getValueAt(baris, 3).toString());
            txtalamat.setText(tabelMahasiswa.getValueAt(baris, 4).toString());
            btnTambah.setEnabled(false);
            btnEdit.setEnabled(true);
            btnHapus.setEnabled(true);
        }
    }//GEN-LAST:event_tabelMahasiswaMouseClicked

