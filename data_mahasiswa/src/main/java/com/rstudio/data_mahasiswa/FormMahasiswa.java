    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(txtnpm.getText().isEmpty() || txtnama.getText().isEmpty() || cojk.getSelectedIndex()==0 || coprodi.getSelectedIndex()==0 || txtalamat.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Inputan belum diisi");
        }else{
            try {
                if(status.equals("tambah")){
                    sql = "insert into mahasiswa values ('" + txtnpm.getText() + "', '" + txtnama.getText() + "', '" + cojk.getSelectedItem().toString() + "', '" + coprodi.getSelectedItem().toString() + "' '" + txtalamat.getText() + "')";
                }else if (status.equals("edit")){
                    sql = "update mahasiswa set nama = '" + txtnama.getText() + "', jenis_kelamin = '" + cojk.getSelectedItem().toString() + "', prodi = '" + coprodi.getSelectedItem().toString() + "' alamat = '" + txtalamat.getText() + "' where npm = '" + txtnpm.getText() + "'";
                }
                pst = conn.prepareStatement(sql);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data barang berhasil disimpan!!");
            } catch (SQLException ex) {
                Logger.getLogger(FormMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }        // TODO add your handling code here: 
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null, "Apakah data barang ini mau dihapus?", "Hapus Data?", JOptionPane.YES_NO_OPTION);
        if(confirm == 0){
            try {
                pst = conn.prepareStatement("delete from mahasiswa where npm = '"+txtnpm.getText() + "'");
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data barang berhasil dihapus");
            } catch (SQLException ex) {
                Logger.getLogger(FormMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
    }//GEN-LAST:event_btnHapusActionPerformed

