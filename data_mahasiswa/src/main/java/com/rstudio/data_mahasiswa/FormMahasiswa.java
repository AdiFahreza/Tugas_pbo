    private void tampil_data(){
        try {
            String[] judul = {"NPM", "Nama", "Jenis Kelamin", "Prodi", "Alamat"};
            DefaultTableModel dtm = new DefaultTableModel(null, judul);
            tabelMahasiswa.setModel(dtm);
            if(txtcari.getText().isEmpty()){
                sql = "select * from mahasiswa";
            }else{
                sql = "select * from mahasiswa where npm like '%" + txtcari.getText() + "%'";
            }
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                String[] data = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

